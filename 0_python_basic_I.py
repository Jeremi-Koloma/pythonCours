# Fundamental Data Types

# int
print(6)
print(type(6))

# float
print(10.50)
print(type(10.50))

# Math function
print(round(3.2))
print(abs(-20))

# Operator precedente
print(20 - 3 * 2)

# Optional bin
print(bin(5))
print(int("0b101", 2))

# Variable
user_age = 1990
first_job = "Designer"
print(user_age)

# Constants
PI = 13.14

# augmented assignment operator
some_value = 5
some_value = some_value + 2
print(some_value)

# string
print(type("Hi hi !"))
username = "supercoder"
password = "supersecret"
print(username)

multi_line = """
first line
second line
third line
"""
print(multi_line)

# string concatenation
print('Hello ' + 'Jeremi')

# Type conversion
print(str(100))

# Escape sequence
water = 'It \'s sunny !'
print(water)

# formatted strings
name = "Jeremi"
print(f'Hi {name}, you are {user_age} years old')